        """–û–±—Ä–∞–±–æ—Ç–∞—Ç—å –Ω–∞–ø–∏—Å–∞–Ω–Ω—É—é —Å—Ç–∞—Ç—å—é"""
        try:
            # –ò–∑–≤–ª–µ–∫–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
            title = self._extract_title(content)
            
            # –û—á–∏—â–∞–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç
            cleaned_content = self._clean_html_content(content)
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –º–∏–Ω–∏–º–∞–ª—å–Ω—É—é –¥–ª–∏–Ω—É
            content_length = len(cleaned_content.replace(' ', ''))
            if content_length < 5000:
                logging.warning(f"–°—Ç–∞—Ç—å—è —Å–ª–∏—à–∫–æ–º –∫–æ—Ä–æ—Ç–∫–∞—è: {content_length} —Å–∏–º–≤–æ–ª–æ–≤ (–º–∏–Ω–∏–º—É–º 5000)")
                # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Å—Ç–∞—Ç—å—é –ø–æ —á–∞—Å—Ç—è–º
                return self._generate_article_by_parts(analysis)
            
            # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –º–µ—Ç–∞–¥–∞–Ω–Ω—ã–µ
            meta_title = self._generate_meta_title(title, analysis)
            meta_description = self._generate_meta_description(cleaned_content)
            excerpt = self._generate_excerpt(cleaned_content)
            
            # –û–ø—Ä–µ–¥–µ–ª—è–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—é
            category = self._determine_category(analysis)
            
            # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º —Ç–µ–≥–∏
            tags = self._generate_tags(analysis, cleaned_content)
            
            # –°–æ–∑–¥–∞–µ–º FAQ
            faq = self._generate_faq(analysis, cleaned_content)
            
            # –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–æ —Ä–∞—Å—à–∏—Ä—è–µ–º –∫–æ–Ω—Ç–µ–Ω—Ç –µ—Å–ª–∏ –æ–Ω –≤—Å–µ –µ—â–µ –∫–æ—Ä–æ—Ç–∫–∏–π
            if content_length < 6000:
                cleaned_content = self._expand_content(cleaned_content, analysis)
                content_length = len(cleaned_content.replace(' ', ''))
                logging.info(f"–ö–æ–Ω—Ç–µ–Ω—Ç —Ä–∞—Å—à–∏—Ä–µ–Ω: {content_length} —Å–∏–º–≤–æ–ª–æ–≤")
            else:
                logging.info(f"–°—Ç–∞—Ç—å—è —Å–æ–∑–¥–∞–Ω–∞: {content_length} —Å–∏–º–≤–æ–ª–æ–≤")
            
            return {
                'title': title,
                'content': cleaned_content,
                'excerpt': excerpt,
                'meta_title': meta_title,
                'meta_description': meta_description,
                'category': category,
                'tags': tags,
                'faq': faq,
                'word_count': content_length,
                'original_analysis': analysis
            }
            
        except Exception as e:
            logging.error(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –∫–æ–Ω—Ç–µ–Ω—Ç–∞ —Å—Ç–∞—Ç—å–∏: {e}")
            return None
    
