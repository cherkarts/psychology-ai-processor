            print("-" * 50)
            print(f"‚ùå –ë—ã–ª–æ: –ù–µ—Ç –∑–∞–≥–æ–ª–æ–≤–∫–∞ —Å—Ç–∞—Ç—å–∏ - {'–ò–°–ü–†–ê–í–õ–ï–ù–û' if has_h1 else '–ù–ï –ò–°–ü–†–ê–í–õ–ï–ù–û'}")
